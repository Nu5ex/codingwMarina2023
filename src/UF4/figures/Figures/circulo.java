package UF4.figures.Figures;

public class circulo {
    public int radi;
    public String color;

    public int getRadi() {
        return radi;
    }

    public void setRadi(int radi) {
        this.radi = radi;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public circulo (int r, String c) {
        r=radi;
        c=color;
    }
}
